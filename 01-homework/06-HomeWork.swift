/*  HomeWork 1.6
հայտարարել հաստատւներ(let)`  a որի արժեքը 400 է,   b որի արժեքը 3 է,
և result որին պետք է վերագրել(=) թե քանի անգամ է մեծ a-ն b-ից (a / b)
 
print()-ի օգնությամբ տպել բոլոր փոփոխակաները:
*/
let a: Int = 400
let b: Int = 3
let result: Int = a / b

print("a-ն b-ից մեծ է \(result) անգամ")
