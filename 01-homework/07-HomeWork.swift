/*  HomeWork 1.7
հայտարարել հաստատւներ(let)`  a որի արժեքը 456 է,   b որի արժեքը 3 է,
և result որին պետք է վերագրել(=) a-ն b-ով բաժանելիս ստացված մնացորդը (a % b)
 
print()-ի օգնությամբ տպել բոլոր փոփոխակաները:
*/

let a: Int = 456
let b: Int = 3
let result: Int = a % b

print("a-ն b-ով բաժանելիս ստացված մնացորդը \(result) է")
